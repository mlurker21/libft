#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int len;
	int i;

	i = 0;
	if (s && f)
	{
		len = ft_strlen(s);
		while (*s != '\0')
			f(i++, s++);
	}
}
